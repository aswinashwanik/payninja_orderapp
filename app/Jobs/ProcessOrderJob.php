    protected $order;

    public function __construct(Order $order)
    {
        $this->order = $order;
    }

    public function handle(): void
    {
        sleep(2); // Simulate delay
        throw new \Exception("Simulated failure for testing.");
        $this->order->update(['status' => 'processed']);
    }
}
